    public emp_dash(int x)
    {
        initComponents();
        String en = "";
        try{
            Class.forName("java.sql.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/trdbms", "root", "");
            Statement stmt = con.createStatement();
            String que = "select ename from employee where eid = "+x+";";
            ResultSet rs = stmt.executeQuery(que);
            while(rs.next())
            {
                en = rs.getString("ename");
            }
            rs.close();
            stmt.close();
            con.close();
   
        }
        catch(Exception e)
        {
            System.out.println(e);
            //JOptionPane.showMessageDialog(null, "Error in connectivity."); 
        }    
        jl1.setText(""+x);
        jl2.setText(en);
    }
